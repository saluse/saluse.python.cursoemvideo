import math
num = float(input('Digite um número: '))
print('O valor digitado foi {} e sua parte inteira é {}'.format(num, math.trunc(num)))

from math import trunc
num1 = float(input('Digite outro número: '))
print('O valor digitado foi {} e sua parte inteira é {}.'.format(num1 , trunc(num1)))

num2 = float(input('digite outro valor: '))
print('O valor digitado foi {} e sua parte inteira é {}.'.format(num2, int(num2)))
